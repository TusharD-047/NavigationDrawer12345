package com.nopalyer.navigationdrawer;

public class uploadPDF {
    public String naMe;
    public String url;

    public uploadPDF() {
    }

    public uploadPDF(String naMe, String url) {
        this.naMe = naMe;
        this.url = url;
    }


    public String getNaMe() {
        return naMe;
    }


    public String getUrl() {
        return url;
    }

}
